"""
Escreva um algoritmo que leia um número entre 100 a 999 e imprima na saída cada um dos algorismos que
compoem o número
"""

num = int(input('Digite um número entre 100 e 999\n'))
n = str(num)

print(f'o primeiro algoritimo é {n[0]}')
print(f'o segundo algoritimo é {n[1]}')
print(f'o terceiro algoritimo é {n[2]}')
