# leia quatro notas e calcule a média aaritimética e imprima o resultado

print('Digite as quatro notas (separadas pelo enter)')
n1 = float(input())
n2 = float(input())
n3 = float(input())
n4 = float(input())
print(f'As quatro notas digitadas foram: {n1}, {n2}, {n3} e {n4}')

media = (n1+n2+n3+n4)/4.0

print(f'A media é {media}')
